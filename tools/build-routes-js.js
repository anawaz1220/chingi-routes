/**
 * build-routes-js.js
 * ───────────────────────────────────────────────────────────────────────────
 * Reads tools/tours-routed.json and writes data/routes.js which defines
 * a ROUTE_COORDS lookup used by script.js instead of injecting into tours.js.
 *
 * Run:  node tools/build-routes-js.js
 * ───────────────────────────────────────────────────────────────────────────
 */

const fs   = require('fs');
const path = require('path');

const ROUTED_FILE = path.join(__dirname, 'tours-routed.json');
const OUT_FILE    = path.join(__dirname, '..', 'data', 'routes.js');

const routed = JSON.parse(fs.readFileSync(ROUTED_FILE, 'utf8'));

const lookup = {};
let total = 0;

for (const group of routed) {
  for (const tour of group.tours) {
    lookup[tour.id] = {};
    for (const day of tour.days) {
      if (!day.routeCoords || day.routeCoords.length < 2) continue;
      lookup[tour.id][day.day] = day.routeCoords;
      total++;
    }
  }
}

const js = `// AUTO-GENERATED by tools/build-routes-js.js — do not edit manually
// Contains pre-computed route coordinates indexed by tourId → dayNumber
const ROUTE_COORDS = ${JSON.stringify(lookup, null, 2)};
`;

fs.writeFileSync(OUT_FILE, js);
console.log(`✅  Wrote data/routes.js — ${total} day segments across ${Object.keys(lookup).length} tours`);
